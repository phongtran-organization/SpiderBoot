package org.netxms.ui.eclipse.logviewer.widgets.helpers;

import org.eclipse.jface.viewers.CellEditor;
import org.eclipse.jface.viewers.CheckboxCellEditor;
import org.eclipse.jface.viewers.ColumnViewer;
import org.eclipse.jface.viewers.EditingSupport;
import org.eclipse.swt.SWT;
import org.netxms.client.log.OrderingColumn;

/**
 * @author Victor
 *
 */
public class OrderingColumnEditingSupport extends EditingSupport
{
	private CellEditor editor;
	
	/**
	 * @param viewer
	 */
	public OrderingColumnEditingSupport(ColumnViewer viewer)
	{
		super(viewer);
		editor = new CheckboxCellEditor(null, SWT.CHECK | SWT.READ_ONLY);
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.EditingSupport#canEdit(java.lang.Object)
	 */
	@Override
	protected boolean canEdit(Object element)
	{
		return true;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.EditingSupport#getCellEditor(java.lang.Object)
	 */
	@Override
	protected CellEditor getCellEditor(Object element)
	{
		return editor;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.EditingSupport#getValue(java.lang.Object)
	 */
	@Override
	protected Object getValue(Object element)
	{
		return new Boolean(((OrderingColumn)element).isDescending());
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.EditingSupport#setValue(java.lang.Object, java.lang.Object)
	 */
	@Override
	protected void setValue(Object element, Object value)
	{
		((OrderingColumn)element).setDescending((Boolean)value);
		getViewer().update(element, null);
	}
}
